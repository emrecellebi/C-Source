#include <iostream>

using namespace std;

int main()
{
	if(10 < 25)
	{
		cout << "10 < 25 = True" << endl;
	}
	
	
	if(10 > 25)
	{
		cout << "10 > 25 = True" << endl;
	}
	else
	{
		cout << "10 > 25 = False" << endl;
	}
	
	
	if(45 <= 25)
	{
		cout << "45 <= 25 = True" << endl;
	}
	else if(25 != 5)
	{
		cout << "25 != 5 = True" << endl;
	}
	else
	{
		cout << "25 != 5 = False" << endl;
	}
	
}
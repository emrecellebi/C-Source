#include <iostream>

using namespace std;

int main()
{
	//	> Büyüktür
	//	< Küçüktür
	//	>= Büyük Eşittir
	//	<= Küçük Eşittir
	//	== Eşit Eşittir
	//	!= Eşit Değilir
	
	cout << (10 > 25) << endl;
	cout << (10 < 5) << endl;
	cout << (10 >= 10) << endl;
	cout << (10 <= 10) << endl;
	cout << (10 == 10) << endl;
	cout << (10 != 20) << endl;
}